#ifndef		STD_TYPES_H
#define		STD_TYPES_H

/*		typedef	(old Data type)	(New data type) ;		*/
/*
		u8 x ;
		unsigned char x;
*/
typedef		unsigned	char			u8	;	
typedef		unsigned	short	int		u16	;	
typedef		unsigned			int		u32	;

typedef		signed		char			s8	;	
typedef		signed		short	int		s16	;	
typedef		signed				int		s32	;	

typedef							float	f64	;	
typedef							double	f128;	


#endif